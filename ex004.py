n = input('Digite algo:')
print('\033[31mO tipo primitivo desse valor é:\033[m',type(n))
print('\033[32mSó tem espaços?\033[m',n.isspace())
print('\033[33mÉ um numero?\033[m',n.isnumeric())
print('\033[34mÉ alfabetico?\033[m',n.isalpha())
print('\033[35mÉ alfanumerico?\033[m',n.isalnum())
print('\033[36mEstá em maiusculas?\033[m',n.isupper())
print('\033[31mEstá em minusculas?\033[m',n.islower())
print('\033[32mEstá capitalizada?\033[m',n.istitle())